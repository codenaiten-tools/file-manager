package com.codenaiten.tools.filemanager.core.adapter.impl;

import com.codenaiten.tools.filemanager.core.adapter.FileContentAdaptable;

public class FileContentAdapter implements FileContentAdaptable {

    private final byte[] value;


// ------------------------------------------------------------------------------------------------------------------ \\
// ---| CONSTRUCTOR |------------------------------------------------------------------------------------------------ \\
// ------------------------------------------------------------------------------------------------------------------ \\

    private FileContentAdapter(final byte[] value ) {
        this.value = value;
    }


// ------------------------------------------------------------------------------------------------------------------ \\
// ---| IMPLEMENTED METHODS |---------------------------------------------------------------------------------------- \\
// ------------------------------------------------------------------------------------------------------------------ \\

    @Override
    public byte[] get() {
        return this.value;
    }


// ------------------------------------------------------------------------------------------------------------------ \\
// ---| BUILDER METHODS |-------------------------------------------------------------------------------------------- \\
// ------------------------------------------------------------------------------------------------------------------ \\

    public static FileContentAdapter of( final String value ) {
        final byte[] bytes = value.getBytes();
        return new FileContentAdapter( bytes );
    }

// ------------------------------------------------------------------------------------------------------------------ \\

}
